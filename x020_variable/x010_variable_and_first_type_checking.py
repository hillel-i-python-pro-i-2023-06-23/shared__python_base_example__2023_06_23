# This is variable. It can store something. It is in-memory storage. App closed, data deleted.
my_variable = "Hello, it is me. Your answer is 42."

# You can output your variable.
print(my_variable)

# You can see "type" of variable.
print(type(my_variable))

# Naming conventions for variables:
#   - Start from "letter" (a-z). Then letter, numbers or underscore. This called "snake_case".
#       Bad: "11variable"
#       Good: "variable"
#   - "underscore" (_) as delimiter.
#       Bad: "myVariable"
#       Good: "my_variable"
#   - Meaningful names are preferred.
#       Bad: "x"
#       Good: "amount_of_apples"
